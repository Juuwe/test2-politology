const container = document.getElementById('questions-container');

questions.forEach((item, index) => {
    const qDiv = document.createElement('div');
    qDiv.className = 'question-block';

    // Оборачиваем текст вопроса в span с классом для стилизации
    qDiv.innerHTML = `<span class="question-text">${index + 1}. ${item.q}</span>`;

    item.a.forEach((answer, i) => {
        const label = document.createElement('label');
        label.className = 'option';
        label.innerHTML = `<input type="radio" name="q${index}" value="${item.keys[i]}" required> ${answer}`;
        qDiv.appendChild(label);
    });

    container.appendChild(qDiv);
});

document.getElementById('submit-btn').onclick = () => {
    const formData = new FormData(document.getElementById('quiz-form'));
    const scores = { 'А': 0, 'Б': 0, 'В': 0, 'Г': 0 };
    let count = 0;

    for (let value of formData.values()) {
        scores[value]++;
        count++;
    }

    if (count < 5) {
        alert("Товарищ, заполните все пункты анкеты! Объект требует полной отчетности.");
        return;
    }

    const winner = Object.keys(scores).reduce((a, b) => scores[a] > scores[b] ? a : b);

    document.getElementById('result-title').innerText = results[winner].title;
    document.getElementById('result-description').innerText = results[winner].desc;

    const modal = document.getElementById('result-modal');
    modal.style.display = 'flex';
    document.getElementById('submit-btn').style.display = 'none';
};
